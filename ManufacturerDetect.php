    protected function _match()
    {

        $user_agent = (isset($_SERVER['HTTP_USER_AGENT']) ? urldecode($_SERVER['HTTP_USER_AGENT']) : '');
        $user_agent = $this->user_agent ? $this->user_agent : $user_agent;

        foreach (self::$mf_list as $mf => $ua_list) {
            foreach ($ua_list as $v) {
                list($ua, $conn) = $v;
                if (stripos($user_agent, $ua) !== false){
                    $this->match = $v;
                    return;
                }
            }
        }
        $this->match = ['Not Identified', ''];
    }

    public function getManufacturer()
    {
        if (!isset($this->match[0])) {
            $this->_match();
        }
        return $this->match[0];
    }

    public function getConnection()
    {
